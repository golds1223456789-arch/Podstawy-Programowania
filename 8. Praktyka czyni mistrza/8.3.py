celsjusze = float(input('Podaj temperature w stopniach Celsjusza: '))
kelvin = celsjusze + 273.15
fahrenheit = (celsjusze * 9/5) + 32
print(f"Temperatura w stopniach Celsjusza (C): {celsjusze:.2f}°C")
print(f"Temperatura w Kelwinach (K):          {kelvin:.2f} K")
print(f"Temperatura w stopniach Fahrenheita (F): {fahrenheit:.2f}°F")
